"""Mathematical operations"""

# Addition
a = 5
b = 3
result = a + b
print("Addition:", result)  # Output: 8

# Subtraction
result = a - b
print("Subtraction:", result)  # Output: 2

# Multiplication
result = a * b
print("Multiplication:", result)  # Output: 15

# Division
result = a / b
print("Division:", result)  # Output: 1.6666666666666667

# Integer Division
result = a // b
print("Integer Division:", result)  # Output: 1

# Modulus
result = a % b
print("Modulus:", result)  # Output: 2

# Exponentiation
result = a**b
print("Exponentiation:", result)  # Output: 125

# Square Root
import math

a = 16
result = math.sqrt(a)
print("Square Root:", result)  # Output: 4.0

# Absolute Value
a = -5
result = abs(a)
print("Absolute Value:", result)  # Output: 5

# Trigonometric Functions
angle = 30  # in degrees
radians = math.radians(angle)

sine = math.sin(radians)
cosine = math.cos(radians)
tangent = math.tan(radians)

print("Sine:", sine)  # Output: 0.49999999999999994
print("Cosine:", cosine)  # Output: 0.8660254037844387
print("Tangent:", tangent)  # Output: 0.5773502691896257

# Rounding a number
test = 2.7
print(round(test))

